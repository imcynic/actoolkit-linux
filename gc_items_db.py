def get_item_name(item_id: int) -> str:
    """Look up an item name by its uint16 ID."""
    return GC_ITEMS.get(item_id, f"Unknown_0x{item_id:04X}")


def get_item_category(item_id: int) -> str:
    """Return the category string for a given item ID."""
    t = (item_id & 0xF000) >> 12
    c = (item_id & 0x0F00) >> 8
    if item_id == 0x0000:
        return "empty"
    if item_id == 0xFFFF:
        return "none"
    if t == 1:
        return "furniture_0"
    if t == 3:
        return "furniture_1"
    if t == 2:
        _cat_map = {
            0: "stationery", 1: "money", 2: "tools", 3: "fish",
            4: "clothing", 5: "misc", 6: "carpet", 7: "wallpaper",
            8: "fruit", 9: "plants", 0xA: "music", 0xB: "diary",
            0xC: "ticket", 0xD: "insect", 0xE: "grab_bag", 0xF: "turnip",
        }
        return _cat_map.get(c, "unknown")
    return "unknown"


def search_items(query: str) -> list[tuple[int, str]]:
    """Search for items by name substring (case-insensitive)."""
    q = query.lower()
    return [(k, v) for k, v in GC_ITEMS.items() if q in v.lower()]


def get_furniture_id(item_id: int, direction: str = "south") -> int:
    """Get the directional variant of a furniture item.

    Furniture items have 4 orientations at consecutive IDs:
      base+0 = South, base+1 = East, base+2 = North, base+3 = West
    """
    base = item_id & ~0x3
    offsets = {"south": 0, "east": 1, "north": 2, "west": 3}
    return base + offsets.get(direction.lower(), 0)


